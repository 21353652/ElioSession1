grade = int ( input ( " Enter the grade : " ))
if grade >= 90 :
    letterGrade = "A"
elif grade >= 80 :
    letterGrade = "B"
elif grade >= 70 :
    letterGrade = "C"
elif grade >= 60 :
    letterGrade = "D"
else:
    letterGrade = "F"
print ( " Your result of grade is : ",letterGrade)
    

    
